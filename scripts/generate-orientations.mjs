import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { PIECE_IDS } from "../packages/core/src/pieceIds.js";
import { PIECE_SHAPES } from "../packages/core/src/pieces.js";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const outputJson = join(root, "packages", "core", "src", "orientations.json");
const outputJs = join(root, "packages", "core", "src", "generatedOrientations.js");

const TRANSFORMS = [
  ([x, y]) => [x, y],
  ([x, y]) => [x, -y],
  ([x, y]) => [-x, y],
  ([x, y]) => [-x, -y],
  ([x, y]) => [y, x],
  ([x, y]) => [y, -x],
  ([x, y]) => [-y, x],
  ([x, y]) => [-y, -x],
];

function compareCells(a, b) {
  for (let index = 0; index < Math.max(a.length, b.length); index += 1) {
    if (!a[index]) return -1;
    if (!b[index]) return 1;
    if (a[index][1] !== b[index][1]) return a[index][1] - b[index][1];
    if (a[index][0] !== b[index][0]) return a[index][0] - b[index][0];
  }
  return 0;
}

function normalizeCells(cells) {
  const minX = Math.min(...cells.map(([x]) => x));
  const minY = Math.min(...cells.map(([, y]) => y));
  return cells
    .map(([x, y]) => [x - minX, y - minY])
    .sort((a, b) => (a[1] - b[1]) || (a[0] - b[0]));
}

function cellKey(cells) {
  return cells.map(([x, y]) => `${x},${y}`).join(";");
}

function getPieceOrientations(pieceId) {
  const baseCells = PIECE_SHAPES[pieceId];
  const unique = new Map();

  for (const transform of TRANSFORMS) {
    const cells = normalizeCells(baseCells.map(transform));
    unique.set(cellKey(cells), cells);
  }

  return Array.from(unique.values())
    .sort(compareCells)
    .map((cells, localId) => ({
      pieceId,
      localId,
      globalId: -1,
      cells,
      width: Math.max(...cells.map(([x]) => x)) + 1,
      height: Math.max(...cells.map(([, y]) => y)) + 1,
      unitSquares: cells.length,
    }));
}

const orientations = PIECE_IDS
  .flatMap((pieceId) => getPieceOrientations(pieceId))
  .map((orientation, globalId) => ({ ...orientation, globalId }));

await mkdir(dirname(outputJson), { recursive: true });
await writeFile(outputJson, `${JSON.stringify(orientations, null, 2)}\n`);
await writeFile(
  outputJs,
  [
    "// Auto-generated by scripts/generate-orientations.mjs",
    `export const ORIENTATIONS = ${JSON.stringify(orientations, null, 2)};`,
    "",
  ].join("\n"),
);

console.log(`Generated ${orientations.length} orientations.`);
